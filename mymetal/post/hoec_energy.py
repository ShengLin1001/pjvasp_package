def run_univ_post(path_out: Path = None) -> None:
    """Run ``pei_vasp_univ_post`` over the whole tree to build the ``y_post_data.txt`` files.

    Mirrors ``mymetal.post.stretch.post_stretch``. It finds every ``y_dir`` below
    ``path_out`` and writes one ``y_post_data.txt`` beside each, i.e. one per mode.

    stdin is fed from /dev/null so the run stays non-interactive: HOEC always has 11 (cubic)
    or 19 (hex) ``y_dir``, which trips ``pei_vasp_univ_post``'s "too many dir, continue?"
    confirmation whenever that guard is enabled.

    Args:
        path_out (Path): The ``y_hoec_energy`` directory.

    Returns:
        None
    """
    path_back = Path.cwd()
    os.chdir(path_out)
    try:
        os.system(UNIV_POST + " < /dev/null")
    finally:
        os.chdir(path_back)      # every caller continues from a known cwd


def load_hoec_manifest(path_out: Path = None) -> dict:
    """Load the ``y_hoec_modes.json`` manifest written by the generator.

    Args:
        path_out (Path): The ``y_hoec_energy`` directory.

    Returns:
        dict: The manifest.
    """
    path_man = Path(path_out) / "y_hoec_modes.json"
    if not path_man.is_file():
        fail("manifest not found: %s (run pei_vasp_run_hoec_energy first)" % path_man)
    return json.loads(path_man.read_text(encoding='utf-8'))


def read_element(path_out: Path = None, dict_manifest: dict = None) -> str:
    """Read the chemical element from a deformed POSCAR (for the literature check).

    Args:
        path_out (Path): The ``y_hoec_energy`` directory.
        dict_manifest (dict): The manifest.

    Returns:
        str: The element symbol, or None when no POSCAR can be read.
    """
    for name, info in dict_manifest["modes"].items():
        p = (Path(path_out) / ("y_hoec_energy_" + name) / "y_dir"
             / info["strain_dirs"][0] / "POSCAR")
        if p.is_file():
            return read(p, format='vasp').get_chemical_symbols()[0]
    return None


def collect_mode_data(path_out: Path = None, dict_manifest: dict = None,
                      fitmax: float = None, run_post: bool = True) -> tuple:
    """Read the energies per mode and convert them to strain-energy densities.

    ``fitmax`` is always given in the units of the *uniaxial reference* mode. When the tree
    was generated with per-mode windows, each mode's cut is ``fitmax * scale``, the same
    factor its window was built with -- so shrinking ``fitmax`` shrinks every mode's window
    proportionally and a Wang-Li Fig. 6 scan stays self-consistent. For a tree generated
    with one shared window (or by an older version), ``scale`` is 1.0 everywhere and this is
    the plain ``|xi| <= fitmax`` cut it always was.

    Args:
        path_out (Path): The ``y_hoec_energy`` directory.
        dict_manifest (dict): The manifest.
        fitmax (float): Max |xi| in reference-mode units; scaled per mode (see above).
        run_post (bool): Re-run ``pei_vasp_univ_post`` before reading. Set False to
            re-fit an already-scraped tree (e.g. when scanning ``fitmax``).

    Returns:
        tuple: ``(dict_data, lmissing)`` where ``dict_data`` maps mode name to
        ``(xi, u_gpa)`` arrays and ``lmissing`` lists strain points without an energy.
    """
    if run_post:
        if shutil.which(UNIV_POST) is None:
            fail("%s not on PATH; source the vasp_utils environment first" % UNIV_POST)
        run_univ_post(path_out)

    v0 = dict_manifest["v0_ang3"]
    conv = vf.phy_const('qe') * 1e21          # eV/A^3 -> GPa, as in mymetal.post.Cij_energy
    dict_data = {}
    lmissing = []
    for name, info in dict_manifest["modes"].items():
        path_mode = Path(path_out) / ("y_hoec_energy_" + name)
        path_data = path_mode / "y_post_data.txt"
        if not path_data.is_file():
            warn("mode %s: no y_post_data.txt; skipped" % name)
            continue

        # jobn are the y_dir subdirectory names, i.e. the strain labels 's_+0.0100'
        jobn, Etot, Eent, pres = vf.vasp_read_post_data(str(path_data))
        dict_e = {j: e for j, e in zip(jobn, Etot)}
        lmissing += ["%s/%s" % (name, lbl) for lbl in info["strain_dirs"]
                     if lbl not in dict_e]

        u0 = dict_e.get("s_%+.4f" % 0.0)      # this mode's own xi=0 reference
        if u0 is None:
            warn("mode %s: no xi=0 reference energy; skipped" % name)
            continue

        # a scaled step does not land on a round grid (mode J: d_xi = 0.00414), and the
        # 4-decimal directory label rounds it. Fit against the exact xi from the manifest;
        # only fall back to the label for trees written before the manifest carried them.
        dict_xi = dict(zip(info["strain_dirs"], info["xi"])) if "xi" in info else {}
        cut = fitmax * info.get("scale", 1.0)

        lxi, lu = [], []
        for lbl, e in dict_e.items():
            xi = dict_xi.get(lbl)
            if xi is None:
                xi = float(lbl.split("_")[1])
            if abs(xi) <= cut + 1e-9:
                lxi.append(xi)
                lu.append((e - u0) * conv / v0)
        dict_data[name] = (np.array(lxi), np.array(lu))
    return dict_data, lmissing


